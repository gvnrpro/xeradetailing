// Generate comprehensive keyword combinations
export const generateKeywordCombinations = (
  service: keyof typeof serviceKeywords,
  location: string = locationKeywords.primary
): string[] => {
  const keywords: string[] = [];
  
  const serviceData = serviceKeywords[service];
  if (!serviceData) return keywords;
  
  // Add all primary keywords
  keywords.push(...serviceData.primary);
  
  // Add secondary keywords with location variations
  serviceData.secondary.forEach(keyword => {
    keywords.push(keyword);
    keywords.push(`${keyword} ${location}`);
    keywords.push(`${keyword} ${locationKeywords.district}`);
  });
  
  // Add long tail keywords
  keywords.push(...serviceData.longTail);
  
  // Add brand combinations
  keywords.push(...brandKeywords.slice(0, 5));
  
  // Add climate-specific combinations
  keywords.push(...climateKeywords.slice(0, 3));
  
  return [...new Set(keywords)]; // Remove duplicates
};

