export type Language = 'en' | 'ml';

export const getTranslation = (key: string, lang: Language = 'en'): string => {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
};
